-module(gutils_app).

%% Application callbacks
-export([get_env/2, get_env/3]).

%%====================================================================
%% API
%%====================================================================

get_env(App, Key) ->
    get_env(App, Key, undefined).

get_env(App, Key, Default) ->
  case application:get_env(App, Key) of
    {ok, Value} ->
            Value;
        _ ->
            Default
  end.

